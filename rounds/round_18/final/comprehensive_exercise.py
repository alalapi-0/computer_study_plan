def mark(task_id: str) -> None:
    subprocess.run(["bash", str(REPO_ROOT / "mark_done.sh"), task_id], cwd=REPO_ROOT, check=True)


def reset_lab() -> None:
    if LAB.exists():
        shutil.rmtree(LAB)
    (LAB / "data").mkdir(parents=True)


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["id", "label", "score", "length", "source"])
        writer.writeheader()
        writer.writerows(rows)


def main() -> None:
    reset_lab()
    (LAB / "README.md").write_text(README, encoding="utf-8")
    (LAB / "analysis.py").write_text(ANALYSIS, encoding="utf-8")
    (LAB / "stdlib_check.py").write_text(STDLIB_CHECK, encoding="utf-8")
    (LAB / "analysis_contract.json").write_text(json.dumps(CONTRACT, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    write_csv(LAB / "data" / "raw_events.csv", RAW_ROWS)
    check = LAB / "final_static_check.py"
    check.write_text(FINAL_CHECK, encoding="utf-8")
    check.chmod(0o755)
    proc = subprocess.run(["python3", str(check)], cwd=LAB, capture_output=True, text=True, check=False)
    if proc.returncode != 0:
        raise RuntimeError(proc.stderr or proc.stdout)
    print("Round 18 综合练习：数值计算与数据分析项目包")
    print("sandbox:", LAB)
    print("contract:", LAB / "analysis_contract.json")
    print("summary:", LAB / "round18_final_summary.json")
    mark("r18-fin-comp")


if __name__ == "__main__":
    main()
