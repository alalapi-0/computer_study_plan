ANALYSIS = '''"""Round 18 final analysis package."""

from __future__ import annotations

import json
from pathlib import Path

import numpy as np
import pandas as pd


RAW_PATH = Path("data/raw_events.csv")
OUT_DIR = Path("output")


def load_events(path: Path = RAW_PATH) -> pd.DataFrame:
    df = pd.read_csv(path)
    print("loaded:", df.shape)
    print("missing:")
    print(df.isna().sum())
    return df


def clean_events(df: pd.DataFrame) -> pd.DataFrame:
    clean = df.dropna(subset=["label", "score", "length"]).copy()
    clean = clean[(clean["score"] >= 0) & (clean["score"] <= 1)]
    clean["quality_band"] = pd.cut(clean["score"], bins=[0, 0.5, 0.8, 1.0], labels=["low", "mid", "high"])
    return clean


def summarize(clean: pd.DataFrame) -> dict:
    score_values = clean["score"].to_numpy()
    return {
        "rows": int(len(clean)),
        "label_counts": clean["label"].value_counts().to_dict(),
        "source_score_mean": clean.groupby("source")["score"].mean().round(3).to_dict(),
        "score_percentiles": {
            "p25": round(float(np.percentile(score_values, 25)), 3),
            "p50": round(float(np.percentile(score_values, 50)), 3),
            "p75": round(float(np.percentile(score_values, 75)), 3),
        },
    }


def main() -> None:
    OUT_DIR.mkdir(exist_ok=True)
    raw = load_events()
    clean = clean_events(raw)
    summary = summarize(clean)
    clean.to_csv(OUT_DIR / "clean_events.csv", index=False)
    (OUT_DIR / "analysis_summary.json").write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")
    print(summary)


if __name__ == "__main__":
    main()
'''

STDLIB_CHECK = '''#!/usr/bin/env python3
"""Dependency-free final preview for the generated analysis package."""

from __future__ import annotations

import csv
import json
import statistics
from pathlib import Path


base = Path(__file__).resolve().parent
with (base / "data" / "raw_events.csv").open("r", encoding="utf-8", newline="") as f:
    rows = list(csv.DictReader(f))

clean = []
for row in rows:
    if not row["label"] or not row["score"] or not row["length"]:
        continue
    score = float(row["score"])
    if 0 <= score <= 1:
        row["score"] = score
        row["length"] = int(row["length"])
        clean.append(row)

label_counts = {}
scores_by_source = {}
for row in clean:
    label_counts[row["label"]] = label_counts.get(row["label"], 0) + 1
    scores_by_source.setdefault(row["source"], []).append(row["score"])

source_score_mean = {
    source: round(statistics.mean(scores), 3)
    for source, scores in sorted(scores_by_source.items())
}
scores = sorted(row["score"] for row in clean)
summary = {
    "raw_rows": len(rows),
    "rows": len(clean),
    "label_counts": label_counts,
    "source_score_mean": source_score_mean,
    "score_min": min(scores),
    "score_max": max(scores),
}
(base / "stdlib_summary.json").write_text(json.dumps(summary, ensure_ascii=False, indent=2) + "\\n", encoding="utf-8")
print(json.dumps(summary, ensure_ascii=False, sort_keys=True))
'''

