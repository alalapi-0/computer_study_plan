#!/bin/bash
# =============================================================
# Round 00 · Week 3 练习脚本
# 主题：man
# 用法：把每个练习的命令在终端里手敲一遍
#       进入 man page 后，看一看，然后按 q 退出
#
# 本脚本对应 progress.json 中的任务：
#   w3-read  → 阅读 week3/notes.md（手动运行：bash mark_done.sh w3-read）
#   w3-ex8   → 练习8：查 man ls
#   w3-ex9   → 练习9：查多个命令的 man page
#   w3-ex10  → 练习10：查 man man
#   w3-self  → 第3周自测
# =============================================================

REPO_ROOT="$(cd "$(dirname "$0")/../../.." && pwd)"

mark() {
  bash "$REPO_ROOT/mark_done.sh" "$1"
}

echo "========================================"
echo "本周提示"
echo "========================================"
echo "进入 man page 后，只需要做这几件事："
echo "  1. 看顶部的 NAME（命令名和简介）"
echo "  2. 看 DESCRIPTION（详细说明）"
echo "  3. 按 q 退出"
echo ""

# -------------------------------------------------------------
# 练习 8：查 man ls
# -------------------------------------------------------------
echo "========================================"
echo "练习 8：查 man ls"
echo "========================================"
echo ">>> 即将打开 man ls"
echo "    进去后：看一下 NAME 和 DESCRIPTION，然后按 q 退出"
echo ""
echo "按回车键继续..."
read
man ls
echo ""
echo "[ 练习8 完成 ] 你顺利退出了吗？"
echo ""
mark w3-ex8

# -------------------------------------------------------------
# 练习 9：依次查多个命令
# -------------------------------------------------------------
echo "========================================"
echo "练习 9：依次查多个命令"
echo "========================================"

echo ">>> 查 man pwd（按回车继续）..."
read
man pwd

echo ""
echo ">>> 查 man mkdir（按回车继续）..."
read
man mkdir

echo ""
echo ">>> 查 man touch（按回车继续）..."
read
man touch

echo ""
echo ">>> 查 man cat（按回车继续）..."
read
man cat

echo ""
echo "[ 练习9 完成 ]"
echo ""
mark w3-ex9

# -------------------------------------------------------------
# 练习 10：查 man man
# -------------------------------------------------------------
echo "========================================"
echo "练习 10：查 man man"
echo "========================================"
echo ">>> 查 man man（按回车继续）..."
read
man man
echo ""
echo "[ 练习10 完成 ] 有点递归的感觉对吧？"
echo ""
mark w3-ex10

# -------------------------------------------------------------
# 第 3 周自测
# -------------------------------------------------------------
echo "========================================"
echo "第 3 周自测"
echo "========================================"
echo "请在终端里独立完成（不看上面的提示）："
echo "  1. 打开 man ls，找到这页在讲什么命令，然后退出"
echo "  2. 打开 man mkdir，然后退出"
echo ""
echo "完成后按回车确认，进度将自动记录..."
read
mark w3-self

echo ""
echo "🎉 Week 3 所有练习完成！"
echo "   请阅读 week3/notes.md，然后运行：bash mark_done.sh w3-read"
